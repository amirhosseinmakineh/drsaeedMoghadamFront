#!/usr/bin/env node
import fs from "node:fs";
import path from "node:path";

const ROOT = process.cwd();
const PUBLIC_CONFIG = path.join(ROOT, "public/web-push-config.js");
const ENV_PROD = path.join(ROOT, "src/environments/environment.prod.ts");

const DEFAULT_VAPID_PUBLIC_KEY =
  "BHrRTag6eomjzkRjtPB4PUKv7RWx08MpTtBslDRei-oev6Ka3ivekjg3Y8GcEf3VZYNxCFW1dYoiewFU5huPiAA";

const publicKey =
  process.env.WEBPUSH_VAPID_PUBLIC_KEY?.trim() || DEFAULT_VAPID_PUBLIC_KEY;
const apiBaseUrl =
  process.env.API_BASE_URL?.trim() ||
  "https://drsaeedback.drmoghadam.runflare.run/api";

const requireConfig =
  process.env.REQUIRE_WEBPUSH_CONFIG === "true" &&
  process.env.SKIP_WEBPUSH_CONFIG_CHECK !== "1";

if (requireConfig && !process.env.WEBPUSH_VAPID_PUBLIC_KEY?.trim()) {
  console.warn(
    "WEBPUSH_VAPID_PUBLIC_KEY not set in build env — using default public key.",
  );
}

const publicConfigContents = `/* generated by scripts/generate-webpush-config.mjs */
self.__WEB_PUSH_VAPID_PUBLIC_KEY__ = ${JSON.stringify(publicKey)};
if (typeof window !== "undefined") {
  window.__WEB_PUSH_VAPID_PUBLIC_KEY__ = ${JSON.stringify(publicKey)};
}
`;

const envProdContents = `export const environment = {
  production: true,
  apiBaseUrl: ${JSON.stringify(apiBaseUrl)},
  webPushVapidPublicKey: ${JSON.stringify(publicKey)},
};
`;

fs.writeFileSync(PUBLIC_CONFIG, publicConfigContents);
fs.writeFileSync(ENV_PROD, envProdContents);

if (publicKey) {
  console.log("Web Push public key written to runtime config.");
} else {
  console.warn(
    "Web Push public key is empty — frontend will try backend /Consultant/WebPushPublicKey.",
  );
}
