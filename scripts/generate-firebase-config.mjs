import { writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const root = join(dirname(fileURLToPath(import.meta.url)), "..");

const REQUIRED_ENV_VARS = [
  "FIREBASE_API_KEY",
  "FIREBASE_AUTH_DOMAIN",
  "FIREBASE_PROJECT_ID",
  "FIREBASE_STORAGE_BUCKET",
  "FIREBASE_MESSAGING_SENDER_ID",
  "FIREBASE_APP_ID",
  "FIREBASE_VAPID_KEY",
];

function env(name, fallback = "") {
  return process.env[name]?.trim() || fallback;
}

const firebase = {
  apiKey: env("FIREBASE_API_KEY"),
  authDomain: env("FIREBASE_AUTH_DOMAIN"),
  projectId: env("FIREBASE_PROJECT_ID"),
  storageBucket: env("FIREBASE_STORAGE_BUCKET"),
  messagingSenderId: env("FIREBASE_MESSAGING_SENDER_ID"),
  appId: env("FIREBASE_APP_ID"),
};

const vapidKey = env("FIREBASE_VAPID_KEY");
const apiBaseUrl = env(
  "API_BASE_URL",
  "https://drsaeedback.drmoghadam.runflare.run/api",
);

const missingVars = REQUIRED_ENV_VARS.filter((name) => !env(name));
const configured = missingVars.length === 0;

const shouldRequireConfig =
  !["1", "true"].includes(process.env.SKIP_FIREBASE_CONFIG_CHECK ?? "") &&
  (process.env.REQUIRE_FIREBASE_CONFIG === "1" ||
    process.env.REQUIRE_FIREBASE_CONFIG === "true" ||
    process.env.CI === "true" ||
    process.env.NETLIFY === "true");

const swConfig = `/* generated by scripts/generate-firebase-config.mjs */
self.__FIREBASE_CONFIG__ = ${JSON.stringify(firebase, null, 2)};
self.__FIREBASE_VAPID_KEY__ = ${JSON.stringify(vapidKey)};
`;

writeFileSync(join(root, "public/firebase-config.js"), swConfig);

const environmentBody = (production) => `export const environment = {
  production: ${production},
  apiBaseUrl: ${JSON.stringify(apiBaseUrl)},
  firebaseVapidKey: ${JSON.stringify(vapidKey)},
  firebase: ${JSON.stringify(firebase, null, 2).replace(/^/gm, "  ")},
};
`;

writeFileSync(
  join(root, "src/environments/environment.prod.ts"),
  environmentBody(true),
);

if (process.env.NODE_ENV !== "production") {
  writeFileSync(
    join(root, "src/environments/environment.ts"),
    environmentBody(false),
  );
}

if (configured) {
  console.log(`Firebase config generated for project: ${firebase.projectId}`);
} else {
  console.warn(
    "Firebase config is empty — set FIREBASE_* env vars before build for push notifications",
  );
  if (missingVars.length) {
    console.warn(`Missing: ${missingVars.join(", ")}`);
  }
}

if (!configured && shouldRequireConfig) {
  console.error(
    "\nProduction/CI build requires Firebase env vars for consultant push notifications.",
  );
  console.error(
    "Set these in Netlify (Site settings → Environment variables) or CI secrets:",
  );
  for (const name of REQUIRED_ENV_VARS) {
    console.error(`  - ${name}`);
  }
  console.error(
    "\nTo skip this check locally, unset CI/NETLIFY or set SKIP_FIREBASE_CONFIG_CHECK=1",
  );
  process.exit(1);
}

if (process.env.SKIP_FIREBASE_CONFIG_CHECK === "1" && !configured) {
  console.warn("SKIP_FIREBASE_CONFIG_CHECK=1 — continuing without Firebase config");
}